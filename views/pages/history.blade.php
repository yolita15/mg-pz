        <h1>
            Константин Величков (1855 - 1907)
        </h1>
        <br>
        <p>
            Константин Величков е роден в Пазарджик през 1855 г. в занaятчийско семейство. Образование получава в
            Цариградския
            императорски лицей "Галата Сарай". Тук се формират неговият характер и мироглед, тук е проникнат от
            идеите за революционна борба. Като лицеист прави първите си творчески опити в областта на литературата.
            Истински успех
            постига с драмата "Невянка и Светослав" през 1874 г., представена в театър "Османие" в Цариград. След
            завършване на
            лицея К.Величков се завръща в родния си град и е назначен за преподавател в класното училище.
            Смел оратор и неуморим организатор, Величков е натоварен с апостолска мисия по подготовката Априлското
            въстание.
            За революционната си дейност е арестуван след избухването на въстанието, окован във вериги и откаран в
            Пловдив,
            а по - късно в Одринския затвор.
        </p>


