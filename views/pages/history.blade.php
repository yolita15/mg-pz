        <p>
            През учебната 1908-1909г. в училището учат 262 ученички, като от тях в по-горен клас минават 142, с
            поправителен изпит са 50, а остават да повтарят годината 69. От 1912г. училището се разраства в девическа
            гимназия. По това време се изучават предметите: вероучение, български език, френски език, руски език,
            психология, краснопис, рисуване, гимнастика, пеене и ръкоделие. През учебната 1923/1924г. съществуващата
            в града мъжка гимназия “Иван Аксаков” и девическата се сливат и образуват смесена гимназия “К. Величков”.
            Със Заповед №2216 от 17
            септ. 1937г. и №2419 от 25 септ. 1937г. на министъра на народното просвещение гимназията се разделя на две
            – мъжка “Ив. Аксаков” и девическа “К. Величков”. От 1939/1940г. ученичките се обучават в реални и
            полукласически паралелки, а от 1941/1942г. и в класически. През 1942/1943г. се
            изучават предметите майчинство и домакинство, чиято цел е да подготвят
            теоретично и практически момичетата за отглеждането на децата.
        </p>

        <p>
            Въз основа на Заповед №1251 от 8 май 1954г. на министъра на народната просвета девическата гимназия се
            трансформира във Втора смесена гимназия «К. Величков». Във връзка с приложението на Закона за по-тясна
            връзка на училището с живота и за по-нататъшно развитие на народното образование в НР България през
            учебната 1959/1960г. училището се преименува във Втора политехническа гимназия "К. Величков".
        </p>

        <p>
            На основание Заповед № Д-4 от 27 май 1971г. на МНП Втора политехническа гимназия се трансформира в
            Математическа. За учебната 1971/1972г. е проведен първият конкурсен изпит за подбор на кандидатите
            за две математически и две физикоматематически паралелки за VIII и IХ клас. Приети са 110 ученика
            за VIII и 117 ученика за IХ клас. От учебната 1981/1982г. гимназията се преименува в Единно средно
            политехническо училище "К. Величков". През учебната 1983/1984г. се прилага нова система на обучение
            във втора и трета степен-УПК с три паралелки по широкопрофилните професии "Оператор-програмист на
            ЕИМ", "Монтьор на електронна техника" и "Оператор в ХВП". Професионалното обучение продължава до
            учебната 1986/1987г. От учебната 1984/85г. в гимназията се разкриват по една паралелка от профилите
            "Физика", "Химия" и "Биология", като паралелките профил "Математика" се намаляват от 6 на 4. Приемният
            изпит е след успешно завършен VII клас по съответния профилиращ предмет.
        </p>

        <p>
            От 1987/88г. до 1992г. се приемат по две паралелки след шести клас по документи. През учебната 1988/89г.
            започва прием за паралелки с разширено изучаване на чужд език, първоначално само на английски, а по-късно
            на немски и на френски език. От учебната 1993/94г. се създава профил "Математика-информатика", а от
            1994/1995г. и профил "Математика-компютърна графика и дизайн". От учебната 1997/98г. е открит нов профил
            "Науки за земята". Поради променените реалности през същата година е прекратен приема за профилите "Физика"
            и "Химия". От учебната 1999/2000г. вече се приемат ученици за профил "Информатика". През 2000/2001 учебна
            година в Математическа гимназия броя на всички ученици е 780 разпределени в 30 паралелки. От 2006/2007г.
            "Информационни технологии" вече е профилиращ предмет, а от 2009/2010г. се осъществява прием на една
            паралелка V-ти клас. През 2011/2012г. в имназията се обучават 725 ученика в 29 паралелки.
        </p>

