        <p>
            На 1 март 1848г. след тържествен водосвет отваря врати първото девическо училище в Пазарджик наречено
            "Левкия води". Първата учителка в училището е монахинята хаджи Татяна от Тетевен. След Освобождението
            училището съществува самостоятелно като девическо трикласно. През 1897г. към него се открива четвърти
            клас и училището приема името на бележития общественик и писател Константин Величков. От 1899г. се
            сформира и пети клас.
        </p>
