        <p>
            През първите три десетилетия след Освобождението 1878 г. К. Величков е една от най-значителните личности в
            нашата обществена действителност - многократно избиран за народен представител, министър на народното
            просвещение в Източна Румелия и по-късно в София. Обаянието му на политически деец е свързано с делото
            му на просветен и културен строител. Даровит артист, той издига на подобаваща висота театралното дело.
            По негово предложение се отпускат стипендии за специализация в чужбина на талантливи български младежи
            - Адриана Будевска, Кръстьо Сарафов, Гено Киров и др. Като министър на просвещението в правителството
            на Константин Стоилов издава правилници и разпоредби за усъвършенствуване организацията и методиката
            на обучение във всички степени на българското училище, създава Висшия учебен съвет и сп. "Училищен преглед".
        </p>

        <p>
            Константин Величков живее с високото съзнание да служи на своя народ и като писател. В следосвобожденската
            литература той е познат като поет, балетрист, мемоарист, драматург, преводач, литературен критик, публицист.
            Автор е на драмите "Опълченец", "На морския бряг", "Хадерал". Свое място в нашата класика има лирическият
            му дневник "Цариградски сонети", а "Писма от Рим" е самобитна по съдържание и форма творба, разкриваща с
            изящна реч за първи път у нас интереса към изобразителното изкуство. Най - значителното произведение на
            Величков са мемоарите му "В темница", исторически документ за моралното величие на българина през бунтовете
            дни на април 1876 г. Константин Величков е автор и на редица преводи от френски, италиански, руски, между
            които най-голяма стойност има преводът му на Дантевия "Ад", ненадминат и до днес в българската литература.
        </p>

        <p>
            Константин Величков сътрудничи на редица периодични издания - вестниците "Лъча" и "Прогрес" в Пазарджик,
            "Народний глас" в Пловдив, на които е и главен редактор, списанията "Наука", "Зора", "Мисъл", "Български
            преглед"
            и много други. По инициатива и под ръководството на Величков в София излизат списанията "Училищен преглед",
            "Ученичиска беседа" и "Летописи".Константин Величков до края остава верен на голямото си влечение към
            рисуването.
            Автор е на живописни творби и портретни скици. Картини на Величков са запазени в художествените галерии на
            София,
            Пловдив, Пазарджик и частни колекции.
        </p>

        <p>
            Съхранил в себе си възрожденския идеализъм, огорчен от обществено - политическия живот в България, Величков
            напуска страната и заминава на лечение в Южна Франция. Умира в Гренобъл на 3.11.1907г.
        </p>
